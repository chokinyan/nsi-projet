import socket
import pygame as pyg
import pyautogui as plt
import time as ti


serveur = socket.socket(socket.AF_INET , socket.SOCK_STREAM)
serveur.bind(('', 80))
serveur.listen(5)
while True:
	client, infosClient = serveur.accept()
	test = client.recv(10**8)	# Reçoit 255 octets. Vous pouvez changer pour recevoir plus de données
	test = test.decode("utf-8")
	
	print(test)
	if test  == "HELLO":
		plt.press("playpause")
		pyg.init()
		win = (1,1)
		pyg.display.set_mode(win,pyg.FULLSCREEN)
		pyg.display.set_caption("AUDIO ENVIRONEMENT")
		pyg.mixer.music.load(r"H:\Mon Drive\projet nsi-20221211T170210Z-001\projet nsi\furnazie.mp3")
		pyg.mixer.music.play()
		pyg.display.flip()
		ti.sleep(5)
		pyg.quit()
	
	client.close()
serveur.close()